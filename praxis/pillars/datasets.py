"""Per-experiment dataset mixtures, rendered to a LaTeX table for the paper.

Reads the newest run per experiment under ``build/runs/`` (the same discovery
as :mod:`runs`), pulls each run's data arguments from its ``spec.json``
(``train_datasets`` collections plus ``--data-path``), resolves collection
names through ``praxis.data.config`` into their member datasets, and writes
``research/datasets.tex`` defining ``\\paperDatasetsTable`` - one row per
dataset per experiment, generalized over source types (huggingface /
directory / synthetic / kb / ...).

Entry point: :func:`export_datasets`, driven by :mod:`praxis.pillars.build`.
Best-effort like the other pillars: no runs (or no resolvable specs) leaves
the paper's empty fallback in place.
"""

import json
import os

from praxis.pillars.runs import (
    OUT_DIR,
    discover_runs,
    experiment_stems,
    latex_escape,
)

OUT_TEX = os.path.join(OUT_DIR, "datasets.tex")


def _spec_args(run_dir: str) -> dict:
    try:
        with open(os.path.join(run_dir, "spec.json")) as fh:
            return json.load(fh).get("args", {}) or {}
    except (OSError, ValueError):
        return {}


def _resolve_rows(args: dict) -> list:
    """[(dataset, type, source, weight)] for one run's training mixture."""
    collections = args.get("train_datasets") or []
    data_path = args.get("data_path") or []
    if isinstance(data_path, str):
        data_path = [data_path]

    rows = []
    try:
        from praxis.data.config import DATASET_COLLECTIONS, DATASETS
    except Exception:
        # Torch-free environment: still record the collection names rather
        # than dropping the table entirely.
        rows.extend((c, "collection", "", None) for c in collections)
        DATASET_COLLECTIONS = DATASETS = None

    if DATASET_COLLECTIONS is not None:
        for coll in collections:
            members = DATASET_COLLECTIONS.get(coll)
            if members is None:
                rows.append((coll, "collection", "(unknown)", None))
                continue
            for name, weight in members.items():
                entry = DATASETS.get(name, {})
                dtype = entry.get("type", "huggingface")
                if dtype.startswith("synthetic-"):
                    dtype = "synthetic"
                source = entry.get("path", "") or "--"
                rows.append((name, dtype, source, weight))

    # --data-path directories: one sampler per path (praxis/data/utils.py),
    # so each is its own row at the directory default weight.
    for p in data_path:
        name = os.path.basename(p.rstrip("/\\")) or p
        rows.append((name, "directory", p, 1.0))
    return rows


def export_datasets(n: int = 4) -> dict:
    """Write datasets.tex for the newest run per experiment (up to ``n``)."""
    selected = []
    for run in discover_runs(experiment_stems())[:n]:
        rows = _resolve_rows(_spec_args(run["dir"]))
        if rows:
            selected.append((run, rows))

    if not selected:
        return {"experiments": 0, "rows": 0}

    # Transpose: datasets down the rows (stable first-appearance order),
    # experiments across the columns, weights in the cells.
    order, info = [], {}
    for col, (run, rows) in enumerate(selected):
        for name, dtype, _source, weight in rows:
            if name not in info:
                order.append(name)
                info[name] = {"type": dtype, "weights": {}}
            info[name]["weights"][col] = weight

    cols = "l l " + "r " * len(selected)
    header = "Dataset & Type & " + " & ".join(
        latex_escape(run["name"]) for run, _ in selected
    )
    # Non-floating (centered tabular + \captionof): the table renders exactly
    # at its hook in the body - a [htbp] float here drifted to the deferred
    # queue and surfaced inside the References section.
    lines = [
        "% Generated by praxis/pillars/datasets.py - do not edit by hand.",
        "\\newcommand{\\paperDatasetsTable}{%",
        "\\par\\begin{center}\\footnotesize",
        f"\\begin{{tabular}}{{{cols.strip()}}}",
        "\\hline",
        header + " \\\\",
        "\\hline",
    ]
    for name in order:
        entry = info[name]
        cells = []
        for col in range(len(selected)):
            w = entry["weights"].get(col)
            cells.append("--" if w is None else f"{w:g}")
        lines.append(
            f"{latex_escape(name)} & {latex_escape(entry['type'])} & "
            + " & ".join(cells)
            + " \\\\"
        )
    lines += [
        "\\hline",
        "\\end{tabular}",
        "\\captionof{table}{Training-data mixtures behind the "
        f"{len(selected)} most recent experiments, resolved from each run's "
        "recorded configuration: named collections expand to their member "
        "datasets, and every \\texttt{--data-path} directory is its own "
        "sampler. Cells are the configured per-dataset sampling weights "
        "(pre-normalization), not token counts; -- marks a dataset absent "
        "from that run's mixture.}",
        "\\label{tab:datasets}",
        "\\end{center}\\par",
        "}",
    ]
    with open(OUT_TEX, "w") as fh:
        fh.write("\n".join(lines) + "\n")
    return {"experiments": len(selected), "rows": len(order)}
